package first_hand_oct_26;

public class For_loop {
	public static void main(String args[]) {
		
	//print no.s from 1to 10
		for(int i= 1;i<=10;i++) {
			System.out.println(i);
		}
		
		for(int j=10;j>=1;j--) {
			System.out.println(j);
		}
		
		//print odd no.s
		for(int a=1;a<=10;a+=2){
			System.out.println(a);
		}
		
		
	}

}
